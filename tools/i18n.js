// i18n utility functions
const i18nUtils = {
  currentLang: 'en',
  
  // Get translation by key
  t: function(key, replacements = {}) {
    const dict = i18n[this.currentLang] || i18n['en'];
    let text = dict[key] || i18n['en'][key] || key;
    
    // Handle replacements like {count}
    Object.keys(replacements).forEach(placeholder => {
      text = text.replace(`{${placeholder}}`, replacements[placeholder]);
    });
    
    return text;
  },
  
  // Set current language
  setLang: function(lang) {
    if (i18n[lang]) {
      this.currentLang = lang;
      localStorage.setItem('asiatek_lang', lang);
      this.updatePage();
      
      // Handle RTL for Arabic
      if (lang === 'ar') {
        document.documentElement.setAttribute('dir', 'rtl');
        document.documentElement.setAttribute('lang', 'ar');
      } else {
        document.documentElement.setAttribute('dir', 'ltr');
        document.documentElement.setAttribute('lang', lang);
      }
    }
  },
  
  // Update all elements with data-i18n attribute
  updatePage: function() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
      const key = el.getAttribute('data-i18n');
      const replacement = el.getAttribute('data-i18n-replace');
      const replacements = replacement ? JSON.parse(replacement) : {};
      
      // Handle different element types
      if (el.tagName === 'INPUT' && el.type === 'text') {
        el.placeholder = this.t(key);
      } else if (el.tagName === 'INPUT' && el.type === 'search') {
        el.placeholder = this.t(key);
      } else if (el.tagName === 'META') {
        el.content = this.t(key);
      } else if (el.tagName === 'TITLE') {
        document.title = this.t(key);
      } else {
        el.textContent = this.t(key, replacements);
      }
    });
  },
  
  // Initialize i18n system
  init: function() {
    // Get saved language or detect
    let savedLang = localStorage.getItem('asiatek_lang');
    if (savedLang && i18n[savedLang]) {
      this.setLang(savedLang);
    }
    
    // Update page immediately if lang is already set
    if (this.currentLang !== 'en') {
      this.updatePage();
    }
  }
};

// Auto-initialize when DOM is ready
document.addEventListener('DOMContentLoaded', function() {
  i18nUtils.init();
});
